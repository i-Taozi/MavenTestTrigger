    /**
     * Gets the value by its code
     *
     * @param code
     *            the code
     * @return the code, or null if no matching value can be found
     */
    public static LanguageID getForCode(String code) {
        for (LanguageID e : values()) {
            if (e.code.equals(code)) {
                return e;
            }
        }
        return null;
    }

